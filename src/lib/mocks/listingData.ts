export const mockCountryGroups: CountryGroup[] = COUNTRY_ORDER.map((countryCode) => {
  const cities = mockCities.filter((city) => city.country === countryCode);
  const trackMix = Array.from(new Set(cities.map((city) => city.serviceTrack)));

  return {
    code: countryCode,
    name: cities[0]?.countryName ?? countryCode,
    cityCount: cities.length,
    trackMix,
  };
});

const trackOrder: Record<ServiceTrack, number> = {
  catchtable: 0,
  rentcar: 1,
  grocery: 2,
};

export function sortTracks(tracks: ServiceTrack[]): ServiceTrack[] {
  return [...tracks].sort((a, b) => trackOrder[a] - trackOrder[b]);
}

